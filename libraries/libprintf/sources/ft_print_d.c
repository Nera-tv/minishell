#include "../ft_printf.h"

int	ft_print_d(int fd, int d)
{
	int	i;

	i = 0;
	ft_putnbr_fd_pf(d, fd);
	if (d == -2147483648)
		return (9);
	if (d < 0)
	{
		i++;
		d = -d;
	}
	while (d > 9)
	{
		d /= 10;
		i++;
	}
	return (i - 1);
}
