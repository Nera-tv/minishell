#include "../includes/minishell.h"

int		if_only_space(t_data *data)
{
	int i;

	i = 0;
	while (data->cmdl[i] != '\0')
	{
		if (data->cmdl[i] != ' ')
			return (1);
		i++;
	}
	return (0);
}
